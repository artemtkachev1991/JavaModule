package First;

public class Exams extends Faculty{
    private String economy;
    private String chemistry;
    private String physics;
    private String history;

    public String getEconomy() {
        return economy;
    }

    public void setEconomy(Matriculant matriculant) {
        this.economy = economy;
    }

    public String getChemistry() {
        return chemistry;
    }

    public void setChemistry(Matriculant matriculant) {
        this.chemistry = chemistry;
    }

    public String getPhysics() {
        return physics;
    }

    public void setPhysics(Matriculant matriculant) {
        this.physics = physics;
    }

    public String getHistory() {
        return history;
    }

    public void setHistory(Matriculant matriculant) {
        this.history = history;
    }
}
